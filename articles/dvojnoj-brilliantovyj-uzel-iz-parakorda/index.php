      </div>
      <p>Удерживая центр пальцами, проведите тёмный шнур по нижней части узла и заведите его в свободное окно между уже уложенными витками.</p>
      <p>Оранжевый шнур при этом остаётся верхней направляющей: он показывает форму будущего узла и помогает не потерять центр.</p>
      <p>Проверьте, что оба цвета чередуются: шнур должен проходить через окно узла, а не просто обматывать его снаружи.</p>

      <p><strong>Совет:</strong> На этом шаге легко ошибиться и провести шнур поверх всего узла. Правильный проход — через внутреннее окно.</p>
    </div>

    <div class="howto-step" id="step-4">
      <span class="step-label">Шаг 4</span>
      <h3>Шаг 4. Используйте шило или фид и выровняйте проходы</h3>
