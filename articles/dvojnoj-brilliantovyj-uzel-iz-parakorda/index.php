      </div>
      <p>Тёмный шнур проведите по тому же принципу: он должен лечь рядом со своим первым проходом и повторить форму петли.</p>
      <p>Работайте по кругу, постепенно выравнивая оба цвета. Следите, чтобы шнуры не пересекались в случайных местах.</p>
      <p>После завершения второго прохода у узла уже будет читаемая форма двойного бриллиантового узла.</p>

      <p><strong>Совет:</strong> Если один цвет начал перекрывать другой, остановитесь и поправьте витки до затяжки. После затягивания исправлять будет намного сложнее.</p>
    </div>

    <div class="howto-step" id="step-7">
      <span class="step-label">Шаг 7</span>
      <h3>Шаг 7. Подтяните, расправьте и зафиксируйте узел</h3>
