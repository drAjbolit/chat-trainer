        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-04-05.webp" alt="Выравнивание проходов с помощью фида при плетении двойного бриллиантового узла" loading="lazy" decoding="async">
      </div>
      <p>Вставьте фид, шило или тонкую спицу в центральное отверстие. Это помогает сохранить форму узла и не стянуть его раньше времени.</p>
      <p>Проведите тёмный шнур через центр и выведите его на противоположную сторону, повторяя траекторию уже уложенного витка.</p>
      <p>Затем аккуратно подтяните оба цвета, но оставьте узел рыхлым: впереди будет второй проход, который и делает узел двойным.</p>

      <p><strong>Совет:</strong> Фид нужен не для усилия, а для контроля формы. Если сильно тянуть на этом этапе, второй слой будет сложно провести.</p>
    </div>

    <div class="howto-step" id="step-5">
      <span class="step-label">Шаг 5</span>
      <h3>Шаг 5. Начните второй проход оранжевым шнуром</h3>
      <div class="step-gallery">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-05-01.webp" alt="Второй проход оранжевого шнура в двойном бриллиантовом узле" loading="lazy" decoding="async">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-05-02.webp" alt="Второй проход оранжевого шнура в двойном бриллиантовом узле" loading="lazy" decoding="async">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-05-03.webp" alt="Второй проход оранжевого шнура в двойном бриллиантовом узле" loading="lazy" decoding="async">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-05-04.webp" alt="Второй проход оранжевого шнура в двойном бриллиантовом узле" loading="lazy" decoding="async">
      </div>
      <p>Теперь оранжевым шнуром повторите траекторию первого слоя. Заведите его рядом с уже уложенным оранжевым витком.</p>
      <p>Не кладите второй проход поверх хаотично: он должен идти параллельно первому витку, формируя двойную стенку узла.</p>
      <p>Протяните оранжевый конец через центральную часть и выведите его вниз, чтобы рисунок начал становиться объёмным.</p>

      <p><strong>Совет:</strong> Главное правило двойного узла: второй проход не заменяет первый, а идёт рядом с ним.</p>
    </div>

    <div class="howto-step" id="step-6">
      <span class="step-label">Шаг 6</span>
      <h3>Шаг 6. Повторите второй проход тёмным шнуром</h3>
      <div class="step-gallery">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-06-01.webp" alt="Второй проход тёмного шнура в двойном бриллиантовом узле" loading="lazy" decoding="async">
