    <p>Двойной бриллиантовый узел — это усложнённая версия классического бриллиантового узла. Основное отличие в том, что каждый шнур проходит по своей траектории дважды, за счёт чего узел получается более объёмным, плотным и выразительным.</p>
    <p>Для сравнения ниже показан обычный бриллиантовый узел из паракорда:</p>
    <img  class="float-left rounded-image" src="images/brilliantovyi-uzel-iz-parakorda-primer-gotovogo-uzla.webp"
