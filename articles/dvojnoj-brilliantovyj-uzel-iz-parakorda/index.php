    <h2 id="steps">Пошаговая инструкция</h2>
    <p>Собирайте узел свободно. Главная ошибка — затянуть его слишком рано. Сначала нужно проверить все проходы, а только потом постепенно выбирать слабину.</p>

    
    <div class="howto-step" id="step-1">
      <span class="step-label">Шаг 1</span>
      <h3>Шаг 1. Подготовьте два шнура и сформируйте первую петлю</h3>
      <div class="step-gallery">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-01-01.webp" alt="Подготовка двух шнуров и первая петля для двойного бриллиантового узла" loading="lazy" decoding="async">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-01-02.webp" alt="Подготовка двух шнуров и первая петля для двойного бриллиантового узла" loading="lazy" decoding="async">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-01-03.webp" alt="Подготовка двух шнуров и первая петля для двойного бриллиантового узла" loading="lazy" decoding="async">
      </div>

<p>Возьмите два отрезка паракорда. В примере один шнур оранжевый, второй тёмный — так легче видеть направление плетения.</p>
      <p>Положите оба шнура рядом и сделайте оранжевым шнуром первую петлю вокруг пальца. Свободный конец оранжевого шнура уведите вправо, 
        чтобы получилась аккуратная рабочая петля.
      Сразу не затягивайте: на этом этапе важно оставить свободное пространство в центре будущего узла.
     
      <strong>Совет:</strong> Если плетёте впервые, используйте два контрастных цвета. Так проще понять, какой шнур куда проходит.</p>
    </div>

    <div class="howto-step" id="step-2">
      <span class="step-label">Шаг 2</span>
      <h3>Шаг 2. Введите второй шнур и соберите основу узла</h3>
      <div class="step-gallery">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-02-01.webp" alt="Введение второго шнура в основу двойного бриллиантового узла" loading="lazy" decoding="async">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-02-02.webp" alt="Введение второго шнура в основу двойного бриллиантового узла" loading="lazy" decoding="async">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-02-03.webp" alt="Введение второго шнура в основу двойного бриллиантового узла" loading="lazy" decoding="async">
      </div>

    <p>Тёмный шнур заведите в образованную оранжевую петлю и проведите его так, чтобы он начал формировать вторую дугу узла.</p>
      <p>Следите, чтобы тёмный шнур не перекручивался: он должен лечь ровно, повторяя округлую форму первой петли.</p>
      <p>После этого у вас получится начальная решётка из двух цветов: оранжевый шнур образует первую петлю, тёмный — вторую.</p>  
      <p><strong>Совет:</strong> Не пытайтесь сразу подтянуть узел. Двойной бриллиантовый узел удобно собирать свободным, а затягивать уже после полной проверки всех проходов.</p>
    </div>

    <div class="howto-step" id="step-3">
      <span class="step-label">Шаг 3</span>
      <h3>Шаг 3. Замкните первый круг переплетения</h3>
      <div class="step-gallery">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-03-01.webp" alt="Первый круг переплетения двойного бриллиантового узла из паракорда" loading="lazy" decoding="async">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-03-02.webp" alt="Первый круг переплетения двойного бриллиантового узла из паракорда" loading="lazy" decoding="async">
        <img  class="rounded-image" src="images/dvoinoi-brilliantovyi-uzel-shag-03-03.webp" alt="Первый круг переплетения двойного бриллиантового узла из паракорда" loading="lazy" decoding="async">
